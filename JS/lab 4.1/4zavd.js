/* 
Дано ширину екрана та кількість елементів, які повинні бути відображені у
рядку. Визначити ширину елементів, які повинні бути відображені у цьому
рядку.
*/
let w,c,wc;

do
{
    w = Number(prompt("ширина екрану = "));
}while(w<0 || isNaN(w));

do
{
    c = Number(prompt("кількість елементів = "));
}while(c<0 || isNaN(c));

wc = w / c;
alert("Ширина елементів що повинні бути відображені у цьому рядку:" + wc);
