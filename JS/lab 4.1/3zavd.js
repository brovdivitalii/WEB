/* 
З клавіатури вводиться кількість монет номіналом 1 копійка. Визначити
скільки гривень і копійок є у даній сумі . Наприклад, дано 245 копійок, а
вивести 2 грн 45 копійок.
*/
let c,g,k;

do 
{
    c = Number(prompt("кількість копійок номіналом 1 = "));
}while(c<0 || isNaN(c))  

g = Math.floor(c/100);
k = c % 100;
alert(g + " гривень " + k + " копійок")